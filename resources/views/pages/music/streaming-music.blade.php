        <h2>Music Streaming Services and... Linux</h2>
        <p>
            So the battle of the music streaming services is on! With all the
            big players vying for "market dominance" us consumers have to parse
            tiny details to figure out which one is right for us. Probably the
            highest-level concerns are:
        </p>
        <ul>
            <li>Platform - what hardware are you using? Mac? Windows? Linux?</li>
            <li>Budget - how much can you afford every month? $0? $20?</li>
            <li>On demand or "radio" services?</li>
        </ul>
        <p>
            In my experience so far, those 3 things narrow things down pretty
            quickly. I am, for example:
        </p>
        <ul>
            <li>A Linux user</li>
            <li>Can reasonably afford $10/mo.</li>
            <li>I prefer on demand service</li>
        </ul>
        <p>
            Being a Linux user immediately cuts out Apple Music, Tidal ... and
            most on demand streaming plans start at $10/mo.
        </p>
        <p>
            PC Mag recently posted their "Best Of" streaming service of 2018
            (<a href="https://www.pcmag.com/article2/0,2817,2380776,00.asp" target="_blank">https://www.pcmag.com/article2/0,2817,2380776,00.asp</a>)
            but they happened to leave out platform as a feature of the various
            services. The first service I actually paid for was Tidal and I quickly
            found out that they will not support Linux. Nope, not even a compatible
            web player. Bummer! So I have to plug in my smartphone
            to a speaker and run the Tidal app from there.
        </p>
        <p>
            Spotify, on the other hand, offers a Linux-compatible web player
            across all tiers. That is compelling for me.
        </p>
        <p>
            So what is my point then? My point is that I'm getting burned out on
            all this technology! If I can't use it the way I want then what is
            the point?
            If my choices of hardware and software are dependent on the services
            I can use them with then that seems backwards to me. It feels like I
            am being dictated to and herded in to what works best for the companies
            and not me.
        </p>
    </section>
