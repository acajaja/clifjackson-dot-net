                This recording showcases Jason's ability at conduction (among other things). Composed sections are woven together in real-time by Jason
                while he's is conducting the orchestra. The result is different every time we play it and is a thorough mix of improvisation
                & composition.
            </p>
            <p>
                <strong>Spontaneous River</strong>
                <br>Personnel:
                <br>In addition to myself there is an entire orchestra (35 - 50) of strings. This includes violin, viola, cello and bass.
            </p>
